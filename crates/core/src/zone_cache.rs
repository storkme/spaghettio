/// Packed entity-kind: belt tier × (surface | UG-in | UG-out). Junction SAT
/// only ever produces these 9 kinds, so a single small int per entity is
/// enough to round-trip name + io_type.
#[repr(u8)]
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum EntityKind {
    BeltYellow = 0,
    BeltRed = 1,
    BeltBlue = 2,
    UgInYellow = 3,
    UgOutYellow = 4,
    UgInRed = 5,
    UgOutRed = 6,
    UgInBlue = 7,
    UgOutBlue = 8,
}

impl EntityKind {
    fn from_entity(e: &PlacedEntity) -> Option<Self> {
        let io = e.io_type.as_deref();
        match (e.name.as_str(), io) {
            ("transport-belt", None | Some("passthrough")) => Some(Self::BeltYellow),
            ("fast-transport-belt", None | Some("passthrough")) => Some(Self::BeltRed),
            ("express-transport-belt", None | Some("passthrough")) => Some(Self::BeltBlue),
            ("underground-belt", Some("input")) => Some(Self::UgInYellow),
            ("underground-belt", Some("output")) => Some(Self::UgOutYellow),
            ("fast-underground-belt", Some("input")) => Some(Self::UgInRed),
            ("fast-underground-belt", Some("output")) => Some(Self::UgOutRed),
            ("express-underground-belt", Some("input")) => Some(Self::UgInBlue),
            ("express-underground-belt", Some("output")) => Some(Self::UgOutBlue),
            _ => None,
        }
    }

    fn name_and_io(self) -> (&'static str, Option<&'static str>) {
        match self {
            Self::BeltYellow => ("transport-belt", None),
            Self::BeltRed => ("fast-transport-belt", None),
            Self::BeltBlue => ("express-transport-belt", None),
            Self::UgInYellow => ("underground-belt", Some("input")),
            Self::UgOutYellow => ("underground-belt", Some("output")),
            Self::UgInRed => ("fast-underground-belt", Some("input")),
            Self::UgOutRed => ("fast-underground-belt", Some("output")),
            Self::UgInBlue => ("express-underground-belt", Some("input")),
            Self::UgOutBlue => ("express-underground-belt", Some("output")),
        }
    }

    fn from_u8(v: u8) -> Option<Self> {
        match v {
            0 => Some(Self::BeltYellow),
            1 => Some(Self::BeltRed),
            2 => Some(Self::BeltBlue),
            3 => Some(Self::UgInYellow),
            4 => Some(Self::UgOutYellow),
            5 => Some(Self::UgInRed),
            6 => Some(Self::UgOutRed),
            7 => Some(Self::UgInBlue),
            8 => Some(Self::UgOutBlue),
            _ => None,
        }
    }
}

fn dir_to_u8(d: EntityDirection) -> u8 {
    match d {
        EntityDirection::North => 0,
        EntityDirection::East => 1,
        EntityDirection::South => 2,
        EntityDirection::West => 3,
    }
}

fn dir_from_u8(v: u8) -> Option<EntityDirection> {
    match v {
        0 => Some(EntityDirection::North),
        1 => Some(EntityDirection::East),
        2 => Some(EntityDirection::South),
        3 => Some(EntityDirection::West),
        _ => None,
    }
}

/// Encode a cache-bound entity into `[kind, x, y, dir, carries_idx]`.
/// `carries_idx` is `-1` for `None`, else the canonical channel position
/// parsed out of the `"chN"` token.
///
/// Returns `None` if the entity isn't a known belt/UG kind or has a `carries`
/// that isn't a `chN` token — i.e. shouldn't have come out of `extract_solution`.
fn entity_to_tuple(e: &PlacedEntity) -> Option<[i32; 5]> {
    let kind = EntityKind::from_entity(e)? as i32;
    let dir = dir_to_u8(e.direction) as i32;
    let carries_idx = match e.carries.as_deref() {
        None => -1,
        Some(t) => t.strip_prefix("ch").and_then(|n| n.parse::<i32>().ok())?,
    };
    Some([kind, e.x, e.y, dir, carries_idx])
}

/// Decode `[kind, x, y, dir, carries_idx]` back into a `PlacedEntity`. The
/// resulting entity has `segment_id: None` (filled in at lookup replay
/// against the new zone's coords).
fn entity_from_tuple(t: [i32; 5]) -> Option<PlacedEntity> {
    let kind = EntityKind::from_u8(u8::try_from(t[0]).ok()?)?;
    let (name, io) = kind.name_and_io();
    let direction = dir_from_u8(u8::try_from(t[3]).ok()?)?;
    let carries = if t[4] < 0 {
        None
    } else {
        Some(format!("ch{}", t[4]))
    };
    Some(PlacedEntity {
        name: name.to_string(),
        x: t[1],
        y: t[2],
        direction,
        io_type: io.map(str::to_string),
        carries,
        ..Default::default()
    })
}

// ---------------------------------------------------------------------------
// Binary record format (cache file `sat-zones.bin`)
// ---------------------------------------------------------------------------
//
// File = sequence of records, each `u32 LE body_len | body[body_len]`.
// Bodies are self-describing (start with `u8 version`) so future schema
// changes can coexist in the same file. Strings are length-prefixed
// `u16 LE len | utf8 bytes`. Entities are 5 bytes:
// `[kind:u8, x:u8, y:u8, dir:u8, carries_idx:i8]`.
//
// Designed to be parseable from a `&[u8]` with no I/O, so the same loader
// works for native (`std::fs::read`) and WASM (`include_bytes!`).

/// Schema version of records emitted by this build. Bumped on incompatible
/// payload changes; the decoder rejects unknown versions.
const RECORD_VERSION: u8 = 1;

/// Telemetry fields decoded from a record. Only `signature` and the
/// cache-payload subset (entities, channel_items, canon_w/h) are needed for
/// cache hits; the rest is for diag tools (histogram, dumps).
#[derive(Debug, Clone)]
pub struct DecodedRecord {
    pub ts: u64,
    pub signature: String,
    pub source: Option<String>,
    pub canon_w: u32,
    pub canon_h: u32,
    pub variables: u32,
    pub clauses: u32,
    pub solve_time_us: u64,
    pub channel_items: Vec<String>,
    pub entities: Vec<PlacedEntity>,
}

/// Append a length-prefixed `u16` UTF-8 string to `buf`. Strings longer than
/// 65535 bytes are truncated — should never happen for our data (signatures
/// are <200 bytes, item names <30 bytes).
fn write_str(buf: &mut Vec<u8>, s: &str) {
    let bytes = s.as_bytes();
    let len = u16::try_from(bytes.len()).unwrap_or(u16::MAX);
    buf.extend_from_slice(&len.to_le_bytes());
    buf.extend_from_slice(&bytes[..len as usize]);
}

/// Read a length-prefixed `u16` UTF-8 string from `slice`, advancing the
/// cursor. Returns `None` if the slice is too short or the bytes aren't UTF-8.
fn read_str(cursor: &mut &[u8]) -> Option<String> {
    if cursor.len() < 2 {
        return None;
    }
    let len = u16::from_le_bytes([cursor[0], cursor[1]]) as usize;
    *cursor = &cursor[2..];
    if cursor.len() < len {
        return None;
    }
    let s = std::str::from_utf8(&cursor[..len]).ok()?.to_string();
    *cursor = &cursor[len..];
    Some(s)
}

fn read_u8(cursor: &mut &[u8]) -> Option<u8> {
    if cursor.is_empty() { return None; }
    let v = cursor[0];
    *cursor = &cursor[1..];
    Some(v)
}

fn read_u16(cursor: &mut &[u8]) -> Option<u16> {
    if cursor.len() < 2 { return None; }
    let v = u16::from_le_bytes([cursor[0], cursor[1]]);
    *cursor = &cursor[2..];
    Some(v)
}

fn read_u32(cursor: &mut &[u8]) -> Option<u32> {
    if cursor.len() < 4 { return None; }
    let v = u32::from_le_bytes([cursor[0], cursor[1], cursor[2], cursor[3]]);
    *cursor = &cursor[4..];
    Some(v)
}

fn read_u64(cursor: &mut &[u8]) -> Option<u64> {
    if cursor.len() < 8 { return None; }
    let mut arr = [0u8; 8];
    arr.copy_from_slice(&cursor[..8]);
    *cursor = &cursor[8..];
    Some(u64::from_le_bytes(arr))
}

/// Encode a complete record (length prefix + body) into `buf`.
///
/// Layout:
/// ```text
/// u32 LE body_len
///   u8       version (=1)
///   u64 LE   ts
///   u32 LE   variables
///   u32 LE   clauses
///   u64 LE   solve_time_us
///   u8       canon_w     (zone dims always ≤ 255)
///   u8       canon_h
///   u16+str  signature
///   u16+str  source      (zero-length = None)
///   u8       n_channels
///   ×n       u16+str channel_items[i]
///   u16 LE   n_entities
///   ×n       [u8 kind, u8 x, u8 y, u8 dir, i8 carries_idx]
/// ```
pub fn encode_record(
    buf: &mut Vec<u8>,
    ts: u64,
    signature: &str,
    source: Option<&str>,
    canon_w: u32,
    canon_h: u32,
    variables: u32,
    clauses: u32,
    solve_time_us: u64,
    channel_items: &[String],
    entities: &[[i32; 5]],
) {
    let mut body: Vec<u8> = Vec::with_capacity(64 + entities.len() * 5);
    body.push(RECORD_VERSION);
    body.extend_from_slice(&ts.to_le_bytes());
    body.extend_from_slice(&variables.to_le_bytes());
    body.extend_from_slice(&clauses.to_le_bytes());
    body.extend_from_slice(&solve_time_us.to_le_bytes());
    body.push(u8::try_from(canon_w).unwrap_or(u8::MAX));
    body.push(u8::try_from(canon_h).unwrap_or(u8::MAX));
    write_str(&mut body, signature);
    write_str(&mut body, source.unwrap_or(""));
    body.push(u8::try_from(channel_items.len()).unwrap_or(u8::MAX));
    for it in channel_items {
        write_str(&mut body, it);
    }
    let n_ent = u16::try_from(entities.len()).unwrap_or(u16::MAX);
    body.extend_from_slice(&n_ent.to_le_bytes());
    for t in entities.iter().take(n_ent as usize) {
        body.push(u8::try_from(t[0]).unwrap_or(0));
        body.push(u8::try_from(t[1]).unwrap_or(0));
        body.push(u8::try_from(t[2]).unwrap_or(0));
        body.push(u8::try_from(t[3]).unwrap_or(0));
        body.push((t[4].clamp(-128, 127)) as i8 as u8);
    }

    let body_len = u32::try_from(body.len()).unwrap_or(u32::MAX);
    buf.extend_from_slice(&body_len.to_le_bytes());
    buf.extend_from_slice(&body);
}

/// Decode the next record from `bytes`, advancing the slice past it. Returns
/// `None` on malformed input or unknown version.
fn decode_record(bytes: &mut &[u8]) -> Option<DecodedRecord> {
    let body_len = read_u32(bytes)? as usize;
    if bytes.len() < body_len {
        return None;
    }
    let mut body = &bytes[..body_len];
    *bytes = &bytes[body_len..];

    let version = read_u8(&mut body)?;
    if version != RECORD_VERSION {
        return None;
    }
    let ts = read_u64(&mut body)?;
    let variables = read_u32(&mut body)?;
    let clauses = read_u32(&mut body)?;
    let solve_time_us = read_u64(&mut body)?;
    let canon_w = read_u8(&mut body)? as u32;
    let canon_h = read_u8(&mut body)? as u32;
    let signature = read_str(&mut body)?;
    let source_raw = read_str(&mut body)?;
    let source = if source_raw.is_empty() { None } else { Some(source_raw) };

    let n_channels = read_u8(&mut body)? as usize;
    let mut channel_items = Vec::with_capacity(n_channels);
    for _ in 0..n_channels {
        channel_items.push(read_str(&mut body)?);
    }
    let n_entities = read_u16(&mut body)? as usize;
    let mut entities = Vec::with_capacity(n_entities);
    for _ in 0..n_entities {
        if body.len() < 5 { return None; }
        let kind = body[0];
        let x = body[1] as i32;
        let y = body[2] as i32;
        let dir = body[3];
        let carries_idx = body[4] as i8 as i32;
        body = &body[5..];
        let tuple = [kind as i32, x, y, dir as i32, carries_idx];
        if let Some(e) = entity_from_tuple(tuple) {
            entities.push(e);
        }
    }

    Some(DecodedRecord {
        ts, signature, source,
        canon_w, canon_h,
        variables, clauses, solve_time_us,
        channel_items, entities,
    })
}

/// Parse a complete byte slice as a sequence of records. Stops at the first
/// malformed record (skipping the rest of the file). Pure-Rust, no I/O —
/// safe to call from WASM with `include_bytes!()` of a pre-baked cache.
pub fn parse_records(mut bytes: &[u8]) -> Vec<DecodedRecord> {
    let mut out = Vec::new();
    while !bytes.is_empty() {
        match decode_record(&mut bytes) {
            Some(rec) => out.push(rec),
            None => break,
        }
    }
    out
}

/// Format a (edge, offset) port endpoint as e.g. `N2` or `E0`.
fn format_endpoint(edge: u8, offset: u32) -> String {
    let e = match edge {
        0 => 'N',
        1 => 'E',
        2 => 'S',
        3 => 'W',
        _ => '?',
    };
    format!("{}{}", e, offset)
}

/// Compute the canonical orientation-invariant signature for a SAT crossing
/// zone.
///
/// Format: `"{W}x{H}:{channels}|F:{forbidden}|UG:{cap}"`
///
/// - `channels`: per-channel tuples `"{in_endpoints}>{out_endpoints}@{reach}"`,
///   joined by `;`. Each endpoint list is sorted (e.g. `N1+S2>E0@5`), so
///   "channel A enters at N1 and S2, exits at E0, has reach 5".
/// - `forbidden`: sorted list of `"{x},{y}"` interior tiles, joined by `;`.
/// - `cap`: `max_ug_ins` value, or `*` for unlimited.
///
/// Tries all 8 D4 symmetries (4 rotations × 2 reflections), transforms ports
/// AND forbidden tiles together, and returns the lex-smallest rendering.
///
/// Thin wrapper around [`canonicalise`] that throws away the orientation tag.
pub fn canonical_signature(
    zone: &CrossingZone,
    channel_reaches: &[u32],
    max_ug_ins: Option<u32>,
) -> String {
    canonicalise(zone, channel_reaches, max_ug_ins).signature
}

/// Result of canonicalising a crossing zone — the signature plus enough
/// orientation metadata to round-trip a stored SAT solution back into the
/// original (or any same-shape) zone's coordinate frame.
#[derive(Debug, Clone)]
pub struct CanonicalForm {
    /// Lex-min rendering across all 8 D4 transforms — the cache key.
    pub signature: String,
    /// Number of 90° clockwise rotations applied to reach the canonical
    /// orientation. 0..4.
    pub rotation: u8,
    /// Whether a vertical-axis reflection is applied (before rotation) to
    /// reach the canonical orientation.
    pub reflect: bool,
    /// Canonical channel ordering: `channel_order[i]` is the source-zone
    /// `channel_id` whose channel string ended up at position `i` in the
    /// sorted-channel list inside the canonical signature.
    ///
    /// Used to map between item names (which live on `channel_id`s in the
    /// source zone) and canonical channel positions (which the cache stores).
    pub channel_order: Vec<u32>,
}

/// Canonicalise a crossing zone: compute its signature plus the orientation
/// and channel mapping required to translate stored SAT solutions back into
/// this zone's frame.
pub fn canonicalise(
    zone: &CrossingZone,
    channel_reaches: &[u32],
    max_ug_ins: Option<u32>,
) -> CanonicalForm {
    // Base port info: (edge, offset, channel_id, is_input)
    let base_ports: Vec<(u8, u32, u32, bool)> = zone
        .boundaries
        .iter()
        .filter_map(|b| {
            let (edge, offset) =
                edge_and_offset(zone.x, zone.y, zone.width, zone.height, b.x, b.y, b.direction)?;
            let edge_idx = match edge {
                Edge::N => 0,
                Edge::E => 1,
                Edge::S => 2,
                Edge::W => 3,
            };
            Some((edge_idx, offset, b.channel_id, b.is_input))
        })
        .collect();

    // Base forbidden tiles in local zone coords.
    let base_forbidden: Vec<(u32, u32)> = zone
        .forced_empty
        .iter()
        .filter_map(|&(wx, wy)| {
            let lx = wx - zone.x;
            let ly = wy - zone.y;
            if lx < 0 || ly < 0 || lx as u32 >= zone.width || ly as u32 >= zone.height {
                return None;
            }
            Some((lx as u32, ly as u32))
        })
        .collect();

    let cap_str = match max_ug_ins {
        None => "*".to_string(),
        Some(k) => k.to_string(),
    };

    // Track each candidate as (signature, rotation, reflect, channel_order).
    let mut candidates: Vec<(String, u8, bool, Vec<u32>)> = Vec::with_capacity(8);

    for rotation in 0u8..4 {
        for &reflect in &[false, true] {
            let (tw, th) = if rotation.is_multiple_of(2) {
                (zone.width, zone.height)
            } else {
                (zone.height, zone.width)
            };

            // Group transformed ports by channel_id.
            let mut by_channel: std::collections::BTreeMap<u32, (Vec<String>, Vec<String>)> =
                Default::default();
            for &(edge_idx, offset, channel_id, is_input) in &base_ports {
                let (ne, no, _, _) =
                    transform_port(edge_idx, offset, zone.width, zone.height, rotation, reflect);
                let ep = format_endpoint(ne, no);
                let entry = by_channel.entry(channel_id).or_default();
                if is_input {
                    entry.0.push(ep);
                } else {
                    entry.1.push(ep);
                }
            }

            // (channel_id, channel_string) — keep the id alongside so we
            // can read the canonical position → id mapping after sorting.
            let mut channel_pairs: Vec<(u32, String)> = by_channel
                .into_iter()
                .map(|(channel_id, (mut ins, mut outs))| {
                    ins.sort_unstable();
                    outs.sort_unstable();
                    let reach = channel_reaches
                        .get(channel_id as usize)
                        .copied()
                        .unwrap_or(0);
                    (
                        channel_id,
                        format!("{}>{}@{}", ins.join("+"), outs.join("+"), reach),
                    )
                })
                .collect();
            // Sort by string only — the channel_id label itself is not
            // canonical (it's the SAT-encoder's allocation order). Tie-break
            // on channel_id so equal strings produce a deterministic order.
            channel_pairs.sort_by(|a, b| a.1.cmp(&b.1).then_with(|| a.0.cmp(&b.0)));

            let channel_order: Vec<u32> = channel_pairs.iter().map(|(id, _)| *id).collect();
            let channel_strs: Vec<String> =
                channel_pairs.into_iter().map(|(_, s)| s).collect();

            let mut fb: Vec<(u32, u32)> = base_forbidden
                .iter()
                .map(|&(x, y)| transform_tile(x, y, zone.width, zone.height, rotation, reflect))
                .collect();
            fb.sort_unstable();
            let fb_str: Vec<String> = fb.iter().map(|(x, y)| format!("{},{}", x, y)).collect();

            let signature = format!(
                "{}x{}:{}|F:{}|UG:{}",
                tw,
                th,
                channel_strs.join(";"),
                fb_str.join(";"),
                cap_str
            );

            candidates.push((signature, rotation, reflect, channel_order));
        }
    }

    // Pick the canonical: lex-min by signature, tie-break on (rotation, reflect)
    // so repeated calls on the same zone always produce the same orientation.
    candidates.sort_by(|a, b| {
        a.0.cmp(&b.0)
            .then_with(|| a.1.cmp(&b.1))
            .then_with(|| a.2.cmp(&b.2))
    });

    let (signature, rotation, reflect, channel_order) = candidates
        .into_iter()
        .next()
        .unwrap_or_else(|| (String::new(), 0, false, Vec::new()));

    CanonicalForm {
        signature,
        rotation,
        reflect,
        channel_order,
    }
}

/// Flush the buffered records to disk.
///
/// Each record is written in a single `write_all` call against an `O_APPEND`
/// file. Lines are kept short (well under the 4 KiB POSIX `PIPE_BUF`
/// guarantee) so concurrent processes appending to the same file produce
/// interleaved-but-intact lines, never torn ones.
///
/// Silently no-ops on any I/O error — losing telemetry is preferable to
/// failing the calling test or CLI invocation.
///
/// Safe to call any number of times; only the records buffered since the
/// previous call are written.
///
/// No-op in WASM builds (no filesystem). The in-memory buffer is never
/// populated there, so this never has anything to do.
#[cfg(not(target_arch = "wasm32"))]
pub fn flush() {
    let pending: Vec<PendingRecord> = match buffer().lock() {
        Ok(mut buf) => std::mem::take(&mut *buf),
        Err(_) => return,
    };
    if pending.is_empty() {
        return;
    }

    let path = resolve_cache_path();
    if let Some(parent) = path.parent() {
        let _ = std::fs::create_dir_all(parent);
    }

    let Ok(mut f) = std::fs::OpenOptions::new()
        .append(true)
        .create(true)
        .open(&path)
    else {
        // Couldn't open — drop the buffer rather than retry-spinning. The
        // records are already lost from the in-memory buffer (we drained
        // it); accept that. A retry path would risk unbounded growth if
        // the disk is permanently unwritable.
        return;
    };

    for record in &pending {
        // O_APPEND + single write_all per record: kernel guarantees the
        // append is atomic for buffers up to PIPE_BUF (4 KiB on Linux).
        // Binary records are typically 100-300 bytes, well under the limit
        // even for the largest zones, so concurrent writers from other
        // processes get interleaved-but-intact records, never torn ones.
        let _ = f.write_all(&record.bytes);
    }
}

/// In WASM builds `flush` is a no-op so callers don't need to cfg-gate.
#[cfg(target_arch = "wasm32")]
pub fn flush() {}

/// Number of records currently buffered, awaiting flush. Useful for sizing
/// diagnostics ("how much will the next flush write?"). Always 0 in WASM.
#[cfg(not(target_arch = "wasm32"))]
pub fn pending_count() -> usize {
    buffer().lock().map(|b| b.len()).unwrap_or(0)
}
#[cfg(target_arch = "wasm32")]
pub fn pending_count() -> usize { 0 }

