//! Persistent + in-memory cache of SAT crossing-zone solutions.
//!
//! Cache file is `sat-zones.bin` (binary records — see `encode_record`).
//! Native builds read/write to `$XDG_CACHE_HOME/fucktorio/sat-zones.bin`
//! (or `FUCKTORIO_ZONE_CACHE_PATH` override); WASM builds load a pre-baked
//! blob via `include_bytes!` instead and never write to disk.
//!
//! The signature captures port connectivity (grouped by channel_id),
//! forbidden interior tiles, per-channel UG reach, and the `max_ug_ins`
//! cap. Two zones with the same signature have identical SAT problems, so
//! a cached solution from one is valid for the other (modulo D4 replay
//! handled by [`lookup_zone`]).
//!
//! Code split: parser, lookup, transforms, and `install_prebaked` are
//! always available. File I/O (`flush`, `load_existing_jsonl`,
//! `resolve_cache_path`, `pending_count`) is gated behind
//! `#[cfg(not(target_arch = "wasm32"))]`.

use crate::models::{EntityDirection, PlacedEntity};
use crate::sat::CrossingZone;

/// Pre-baked cache embedded in WASM builds. Generated by running
/// `diag_corpus_sweep` against the e2e corpus and copying
/// `~/.cache/fucktorio/sat-zones.bin` to `crates/core/data/`.
#[cfg(target_arch = "wasm32")]
const EMBEDDED_CACHE: &[u8] = include_bytes!("../data/sat-zones.bin");

/// Internal edge classification used only for the canonical signature
/// computation.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum Edge {
    N,
    E,
    S,
    W,
}

/// Derive `(edge, offset)` for a port at `(px, py)` on the boundary of a
/// `(width, height)` zone anchored at `(zx, zy)`. Returns `None` if the
/// port point is outside the zone bbox.
fn edge_and_offset(
    zx: i32,
    zy: i32,
    w: u32,
    h: u32,
    px: i32,
    py: i32,
    fallback_dir: crate::models::EntityDirection,
) -> Option<(Edge, u32)> {
    let lx = px - zx;
    let ly = py - zy;
    if lx < 0 || ly < 0 || lx as u32 >= w || ly as u32 >= h {
        return None;
    }
    let lxu = lx as u32;
    let lyu = ly as u32;
    let on_north = lyu == 0;
    let on_south = lyu == h - 1;
    let on_west = lxu == 0;
    let on_east = lxu == w - 1;
    let edge = match (on_north, on_south, on_west, on_east) {
        (true, false, false, false) => Edge::N,
        (false, true, false, false) => Edge::S,
        (false, false, true, false) => Edge::W,
        (false, false, false, true) => Edge::E,
        // Corner/degenerate — break ties with flow direction.
        _ => {
            use crate::models::EntityDirection;
            match fallback_dir {
                EntityDirection::North => Edge::N,
                EntityDirection::South => Edge::S,
                EntityDirection::West => Edge::W,
                EntityDirection::East => Edge::E,
            }
        }
    };
    let offset = match edge {
        Edge::N | Edge::S => lxu,
        Edge::E | Edge::W => lyu,
    };
    Some((edge, offset))
}

